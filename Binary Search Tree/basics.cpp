// A Binary Search Tree (BST) is a special type of binary tree with the following properties :-
// Each node has at most two children: a left child and a right child.
// For any node in the tree :- All the values in the left subtree are less than the value of the node. All the values in the right subtree are greater than or equal to the value of the node.
// Eg of a Binary Search Tree :-
//          50
//         /  \
//       30    70
//      / \    / \
//    20  40  60  80


// When a BST is traversed using in-order traversal (left subtree → root → right subtree), the elements are visited in ascending sorted order.

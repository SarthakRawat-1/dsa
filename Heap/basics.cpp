// A heap is a specialized tree‐based data structure that satisfies two properties :-

// Shape (Complete Binary Tree)
// Every level, except possibly the last, is completely filled, and all nodes are as far left as possible.
// The last level is filled from left to right (i.e. you never leave a “gap” in the middle of the bottom row).

// Heap‐Order (Heap Property)
// Max-Heap: Every parent node’s key (value) ≥ its children’s keys (value).
// Min-Heap: Every parent node’s key (value) ≤ its children’s keys (value).